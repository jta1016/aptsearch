def haversine(lat1: float, lng1: float, lat2: float, lng2: float) -> float:
    """Distance in miles between two lat/lng points."""
    R = 3958.8  # Earth radius in miles
    phi1, phi2 = math.radians(lat1), math.radians(lat2)
    dphi = math.radians(lat2 - lat1)
    dlambda = math.radians(lng2 - lng1)
    a = math.sin(dphi / 2) ** 2 + math.cos(phi1) * math.cos(phi2) * math.sin(dlambda / 2) ** 2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def nearest_station(lat: float, lng: float) -> dict:
    """Return the nearest subway station and its distance in miles."""
    best = None
    best_dist = float("inf")
    for name, slat, slng, lines in STATIONS:
        d = haversine(lat, lng, slat, slng)
        if d < best_dist:
            best_dist = d
            best = {"name": name, "lat": slat, "lng": slng, "lines": lines, "distance_miles": round(d, 3)}
    return best


def subway_score(lat: Optional[float], lng: Optional[float], max_miles: float, preferred_lines: list) -> tuple[float, Optional[dict]]:
    """
    Returns (score 0-1, nearest_station_info).
    Score is 0 if beyond max_miles, scales to 1.0 at 0 distance.
    Bonus if preferred lines are served.
    """
    if lat is None or lng is None:
        return 0.75, None  # neutral if no coordinates — don't penalize missing geodata

    station = nearest_station(lat, lng)
    dist = station["distance_miles"]

    if dist > max_miles:
        return 0.0, station

    # Base score: linear decay from 1.0 at 0 miles to 0.0 at max_miles
    base = 1.0 - (dist / max_miles)

    # Bonus for preferred lines (up to 0.2)
    if preferred_lines:
        matches = len(set(preferred_lines) & set(station["lines"]))
        line_bonus = min(0.2, matches * 0.1)
        base = min(1.0, base + line_bonus)

    return round(base, 3), station
